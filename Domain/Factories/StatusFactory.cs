using Data.Entities;
using Domain.Models;

namespace Data.Factories;

public static class StatusFactory
{
    public static StatusEntity CreateEntityFromModel(Status model)
    {
        return new StatusEntity
        {
            StatusName = model.StatusName
        };
    }

    public static Status CreateModelFromEntity(Status entity)
    {
        return new Status
        {
            Id = entity.Id,
            StatusName = entity.StatusName
        };
    }
}
